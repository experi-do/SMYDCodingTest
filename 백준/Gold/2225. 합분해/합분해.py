n, k = map(int, input().split())

memo = [[0 for _ in range(n+1)] for _ in range(k+1)]
for i in range(n+1):
    memo[1][i] = 1

for i in range(1, k+1):
    memo[i][0] = 1

for i in range(2, k+1):
    for j in range(1, n+1):
        memo[i][j] = (memo[i-1][j] + memo[i][j-1]) % 1000000000

print(memo[k][n])